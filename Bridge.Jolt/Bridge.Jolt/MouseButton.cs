namespace Jolt
{
    /// <summary>
    /// Provides information about a mouse button.
    /// </summary>
    public enum MouseButton
    {
        /// <summary>
        /// Represents the left/primary mouse button.
        /// </summary>
        Left = 0,

        /// <summary>
        /// Represents other/secondary mouse buttons.
        /// </summary>
        Other = 1
    }
}